
/**
* op_add - function to sum two numbers
* @a: arg1
* @b: arg2
* Return: add a + b
**/

int op_add(int a, int b)
{
	return (a + b);
}


/**
* op_sub - function to subtract two numbers
* @a: arg1
* @b: arg2
* Return: add a - b
**/

int op_sub(int a, int b)
{
	return (a - b);
}



/**
* op_mul - function to multiply two numbers
* @a: arg1
* @b: arg2
* Return: add a * b
**/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - function to divide two numbers
* @a: arg1
* @b: arg2
* Return: add a / b
**/

int op_div(int a, int b)
{
	return (a / b);
}



/**
* op_mod - function to return the remainder of a division
* @a: arg1
* @b: arg2
* Return: add a % b
**/

int op_mod(int a, int b)
{
	return (a % b);
}

